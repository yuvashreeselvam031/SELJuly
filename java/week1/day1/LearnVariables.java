package week1.day1;

public class LearnVariables {
	public static void main(String[] args) {
		// mobile properties
		// primitive data types
		// syntax datatype variablename = value;
		byte screenSize = 14;
		short price = 25000;
		int regnum = 29434171;
		long mobileNum = 9941317123l;
		float current = 22.5f;
		double batterySize = 12.4759694648;
		char brand = 'A';
		boolean isSmartPhone = true;

		System.out.println("screenSize= " + screenSize);
		System.out.println("price= " + price);
		System.out.println("regnum= " + regnum);
		System.out.println("mobileNum= " + mobileNum);
		System.out.println("current= " + current);
		System.out.println("batterySize= " + batterySize);
		System.out.println("brand= " + brand);
		System.out.println("isSmartPhone= " + isSmartPhone);
		// non primitive data types
		String str = "non primitive datatypes";
		System.out.println("str= " + str);
	}

}
